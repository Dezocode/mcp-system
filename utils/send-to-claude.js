    // Parse the error log
    function parseErrorLog(logText) {
        const lines = logText.split('\n');
        const errors = [];
        let i = 0;

        while (i < lines.length) {
            const line = lines[i];
            if (line.trim() === '') {
                i++;
                continue;
            }
            
            if (line === 'App Errors' || line === 'Test Pipeline') {
                const category = line;
                i++;
                if (i < lines.length && lines[i].match(/^\d{1,2}:\d{2}:\d{2}\s*[AP]M$/)) {
                    const timestamp = lines[i];
                    i++;
                    if (i < lines.length) {
                        const message = lines[i];
                        errors.push({ category, timestamp, message });
                    }
                }
            }
            i++;
        }
        return errors;
    }

    // Format for Claude
    function formatForClaude(errors) {
        const errorsByCategory = {};
        errors.forEach(error => {
            if (!errorsByCategory[error.category]) {
                errorsByCategory[error.category] = [];
            }
            errorsByCategory[error.category].push(error);
        });

        let output = `I have an error log with ${errors.length} errors that need analysis:\n\n`;
        
        for (const [category, categoryErrors] of Object.entries(errorsByCategory)) {
            output += `**${category}** (${categoryErrors.length} occurrences):\n`;
            const sample = categoryErrors[0];
            output += `- First occurrence: ${sample.timestamp}\n`;
            output += `- Error: ${sample.message.substring(0, 100)}...\n\n`;
        }
        
        output += `The main error appears to be: "Error invoking remote method 'GUEST_VIEW_MANAGER_CALL': Error: ERR_ABORTED (-3) loading 'about:blank'"\n\n`;
        output += `Can you help me understand what's causing this error and how to fix it?`;
        
        return output;
    }

    // Send to Claude
    function sendToClaude() {
        const errors = parseErrorLog(ERROR_LOG);
        const message = formatForClaude(errors);
        
        // Find the input field
        const inputField = document.querySelector('div[contenteditable="true"].ProseMirror') || 
                          document.querySelector('div[contenteditable="true"]');
        
        if (inputField) {
            // Set the content
            inputField.innerHTML = message.replace(/\n/g, '<br>');
            inputField.dispatchEvent(new Event('input', { bubbles: true }));
            
            // Find and click send button after a short delay
            setTimeout(() => {
                const sendButton = document.querySelector('button[aria-label*="Send"]') ||
                                 Array.from(document.querySelectorAll('button')).find(btn => 
                                     btn.innerHTML.includes('svg') && !btn.disabled
                                 );
                
                if (sendButton) {
                    sendButton.click();
                    console.log('✅ Error log sent to Claude!');
                } else {
                    console.log('⚠️ Send button not found. Press Enter to send.');
                }
            }, 500);
        } else {
            console.error('❌ Could not find Claude input field');
            console.log('📋 Message copied to clipboard instead:');
            console.log(message);
            navigator.clipboard.writeText(message);
        }
    }

    // Execute
    sendToClaude();
